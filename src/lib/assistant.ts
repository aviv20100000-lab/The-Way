const FOOD_SEARCH_TOOL: Anthropic.Tool = {
  name: "search_food",
  description:
    "חיפוש ערכים תזונתיים במאגר צמרת הרשמי של משרד הבריאות. מחזיר עד 8 מוצרים תואמים עם קלוריות, חלבון, פחמימות ושומן ל-100 גרם. השתמש כשנשאלת על מוצר ספציפי או השוואת מוצרים.",
  input_schema: {
    type: "object",
    properties: {
      query: {
        type: "string",
        description: "שם המזון בעברית, למשל: קוטג', גבינה צהובה, חזה עוף",
      },
    },
    required: ["query"],
  },
};

async function runFoodSearch(input: unknown): Promise<string> {
  const query = typeof (input as { query?: unknown })?.query === "string"
    ? ((input as { query: string }).query)
    : "";
  if (!query.trim()) return JSON.stringify({ results: [] });
  try {
    const foods = await searchTzameret(query);
    return JSON.stringify({
      results: foods.map((food) => ({
        name: food.name_he,
        per_100g: {
          calories: Math.round(food.calories),
          protein_g: Math.round(food.protein * 10) / 10,
          carbs_g: Math.round(food.carbs * 10) / 10,
          fat_g: Math.round(food.fat * 10) / 10,
        },
      })),
    });
  } catch {
    return JSON.stringify({ results: [], error: "search failed" });
  }
}

const MAX_TOOL_ROUNDS = 3;

export async function generateAssistantReply(
  context: AssistantUserContext,
  history: AssistantHistoryMessage[],
  userMessage: string
): Promise<string> {
  const messages: Anthropic.MessageParam[] = [
    ...history.map((message) => ({ role: message.role, content: message.content })),
    { role: "user" as const, content: userMessage },
  ];

  const request = () =>
    createMessage({
      model: "claude-haiku-4-5",
      max_tokens: 700,
      temperature: 0.5,
      system: `${SYSTEM_PROMPT}\n\n${HEBREW_TONE_GUIDE}\n\n${buildContextBlock(context)}`,
      tools: [FOOD_SEARCH_TOOL],
      messages,
    });

  let response = await request();

  for (let round = 0; round < MAX_TOOL_ROUNDS && response.stop_reason === "tool_use"; round += 1) {
    const toolUses = response.content.filter(
      (block): block is Anthropic.ToolUseBlock => block.type === "tool_use"
    );
    const toolResults: Anthropic.ToolResultBlockParam[] = await Promise.all(
      toolUses.map(async (toolUse) => ({
        type: "tool_result" as const,
        tool_use_id: toolUse.id,
        content: await runFoodSearch(toolUse.input),
      }))
    );
    messages.push({ role: "assistant", content: response.content });
    messages.push({ role: "user", content: toolResults });
    response = await request();
  }

  const text = response.content
    .filter((block): block is Anthropic.TextBlock => block.type === "text")
    .map((block) => block.text)
    .join("\n")
    .trim();

  return text || "לא הצלחתי לענות הפעם — נסה לנסח שוב, או שאל את המאמן 🙏";
}
